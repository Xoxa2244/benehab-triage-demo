          // Ğ”Ğ»Ñ Ñ†Ğ¸ĞºĞ»Ğ¾Ñ‚Ğ¸Ğ¼Ğ¸Ñ‡ĞµÑĞºĞ¾Ğ³Ğ¾ Ñ‚Ğ¸Ğ¿Ğ° Ğ´Ğ¾Ğ±Ğ°Ğ²Ğ»ÑĞµĞ¼ Ğ¸Ğ½ÑÑ‚Ñ€ÑƒĞºÑ†Ğ¸Ğ¸ Ğ´Ğ»Ñ Ğ¾Ğ±ĞµĞ¸Ñ… Ñ„Ğ°Ğ·
          instructions.general.key_points.push(...typeInstructions.positive_communication.up_phase);
